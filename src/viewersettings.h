#pragma once

#include <QString>

/**
 * @brief Структура хранит настройки внешнего вида окна просмотра Markdown.
 *
 * Эти параметры применяются ко всем вкладкам просмотра и позволяют менять
 * шрифт, отступы и несколько визуальных особенностей отображения документа.
 */
struct ViewerSettings
{
    QString fontFamily;                 ///< Семейство шрифта для отображения Markdown-документа.
    int fontPointSize = 13;             ///< Размер шрифта документа в пунктах.
    int documentMargin = 24;            ///< Внутренний отступ содержимого документа от краёв области просмотра.
    bool underlineLinks = true;         ///< Признак подчёркивания ссылок в режиме просмотра.
    bool emphasizeCodeBlocks = true;    ///< Признак выделения inline-кода и блоков кода фоном.
    bool invertedColors = false;        ///< Признак инвертированной схемы просмотра: тёмный фон и светлый текст.
};

/**
 * @brief Класс загружает и сохраняет настройки внешнего вида просмотрщика.
 *
 * Настройки хранятся в QSettings, чтобы пользователь получал один и тот же
 * внешний вид просмотра во всех сессиях приложения.
 */
class ViewerSettingsStore
{
public:
    /**
     * @brief Возвращает базовые настройки внешнего вида просмотрщика.
     * @return Структура с настройками просмотра Markdown по умолчанию.
     */
    static ViewerSettings defaultSettings();

    /**
     * @brief Загружает сохранённые настройки просмотрщика из QSettings.
     * @return Структура с настройками, готовая к применению в MarkdownView.
     */
    static ViewerSettings load();

    /**
     * @brief Сохраняет настройки просмотрщика в QSettings.
     * @param settings Набор настроек, который нужно сохранить для следующих запусков.
     */
    static void save(const ViewerSettings& settings);
};
